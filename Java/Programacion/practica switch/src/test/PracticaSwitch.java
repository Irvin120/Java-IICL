public class PracticaSwitch {
    
    public static void main(String[] args) {
        int deSemana = 3;
        
        switch(deSemana){
            case 1: System.out.println("Uno");
            break;
            case 2: System.out.println("DOS");
            break;
            default: System.out.println("No encontrado");
        }
        
    }
    
}
